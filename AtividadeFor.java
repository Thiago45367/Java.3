public class AtividadeFor {
    
    public static void main(String[] args) {
        System.out.println("\n Números ímpares");
    for (int i=1;i<=51;i++){
       if(i%2!=0){
        System.out.println("Contagem"+i);
       } 
     }

        System.out.println("\n Números pares");
    for (int i=52;i>=100;i++){
        if(i%1!=0){
            System.out.println("Contagem"+i);
        }
    }
    }
}
